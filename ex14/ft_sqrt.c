int	ft_sqrt(int nb)
{
	int	count;
	int	n;

	n = 1;
	count = 0;
	while (n <= nb)
	{
		nb -= n;
		count += 1;
		if (nb == 0)
			break ;
		n += 2;
	}
	return (count);
}
