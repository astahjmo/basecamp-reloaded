#define MAX_INT_ROOT 46340

int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	while ((x * x) < nb && x <= MAX_INT_ROOT)
		x++;
	if ((x * x) == nb)
		return (x);
	return (0);
}
